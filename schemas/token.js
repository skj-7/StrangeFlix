var mongoose = require('mongoose');

const tokenSchema = new mongoose.Schema({
	_userId: {
		type: mongoose.Schema.Types.ObjectId,
		required: true,
		ref: 'User'
	},
	token: {
		type: String,
		required: true
	},
	createdAt: {
		type: Date,
		required: true,
		default: Date.now,
		expires: 300
	}
});

var usertoken = mongoose.model('token', tokenSchema);

module.exports = usertoken;